#include<stdio.h>
int main()
{
    int a=12,b=6;
    printf("a + b = %d\n",a + b);
    //     12 + 6
    printf("a - b = %d\n",a - b);
    //     12 - 6
    printf("a * b = %d\n",a * b);
    //     12 * 6
    printf("a / b = %d\n",a / b);
    //     12 / 6
    printf("a %% b = %d\n",a % b);
    return 0;
}